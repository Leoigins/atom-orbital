
"""
Atomic orbital database for ground-state atoms H–Kr.

Source basis data
-----------------
- Neutral-atom Roothaan–Hartree–Fock tables from Clementi & Roetti (1974)
  for He–Ar (the original scope of this file).
- Bunge, Barrientos, and Bunge, Atomic Data and Nuclear Data Tables 53,
  113 (1993), pages 127–144, for K–Kr (the period-4 extension).
- The Bunge paper begins at Z = 2, so H(1s) is supplemented here with the
  exact hydrogenic 1s orbital in atomic units.

The naming ``atomic_orbitals_h_to_ar`` is preserved for backward compatibility
with code that imports from this module; the database itself now extends to Kr.

Conventions
-----------
1) Radial basis functions follow the paper:
       X_{p,l,m}(r,theta,phi) = R_{p}(r) Y_{l,m}(theta,phi)
   with
       R_p(r) = N * r^(n_basis-1) * exp(-zeta * r)
       N = ((2*zeta)^(2*n_basis + 1) / (2*n_basis)!)**0.5
   which is equivalent to Eq. (6) in the paper.

2) The stored coefficients are the tabulated expansion coefficients C.
   Therefore, the atomic radial orbital is:
       R_orb(r) = sum_p C_p * R_p(r)

3) Angular functions are generated analytically from spherical harmonics.
   The paper tabulates normalized complex Y_{l,m}; for chemistry plotting,
   real spherical harmonics are often more convenient, so both are supported.

Units
-----
Atomic units by default:
- r in bohr
- orbital energies in hartree
"""

from __future__ import annotations

from dataclasses import dataclass
from math import factorial, sqrt, pi
from typing import Dict, List, Tuple

import numpy as np
from scipy.special import sph_harm


@dataclass(frozen=True)
class BasisTerm:
    n_basis: int
    zeta: float
    coeff: float


@dataclass(frozen=True)
class OrbitalData:
    label: str
    l: int
    energy_hartree: float
    basis: Tuple[BasisTerm, ...]


@dataclass(frozen=True)
class ElementData:
    Z: int
    symbol: str
    name: str
    term_symbol: str
    configuration: str
    source: str
    orbitals: Dict[str, OrbitalData]


def sto_radial(n_basis: int, zeta: float, r):
    """
    Normalized Slater-type radial factor used in the paper:
        R(r) = N r^(n-1) exp(-zeta r)
    """
    r = np.asarray(r, dtype=float)
    norm = ((2.0 * zeta) ** (2 * n_basis + 1) / factorial(2 * n_basis)) ** 0.5
    return norm * np.power(r, n_basis - 1) * np.exp(-zeta * r)


def orbital_radial(symbol: str, orbital: str, r):
    """
    Radial orbital R_{n,l}(r) in atomic units.
    """
    data = ATOMIC_DB[symbol].orbitals[orbital]
    total = np.zeros_like(np.asarray(r, dtype=float), dtype=float)
    for term in data.basis:
        total = total + term.coeff * sto_radial(term.n_basis, term.zeta, r)
    return total


def radial_probability_density(symbol: str, orbital: str, r):
    """
    Radial probability density P(r) = r^2 |R(r)|^2.
    """
    r = np.asarray(r, dtype=float)
    R = orbital_radial(symbol, orbital, r)
    return r**2 * np.abs(R) ** 2


def complex_spherical_harmonic(l: int, m: int, theta, phi):
    """
    Complex normalized spherical harmonic Y_l^m(theta, phi).

    theta: polar angle in [0, pi]
    phi:   azimuthal angle in [0, 2*pi)
    """
    theta = np.asarray(theta, dtype=float)
    phi = np.asarray(phi, dtype=float)
    # scipy.special.sph_harm uses arguments (m, l, phi, theta)
    return sph_harm(m, l, phi, theta)


def real_spherical_harmonic(l: int, m: int, theta, phi):
    """
    Real (chemistry-style) normalized spherical harmonic.

    Mapping:
      m =  0 -> Y_l^0
      m >  0 -> sqrt(2)(-1)^m Re[Y_l^m]
      m <  0 -> sqrt(2)(-1)^m Im[Y_l^{|m|}]
    """
    if abs(m) > l:
        raise ValueError("Need |m| <= l.")
    Y = complex_spherical_harmonic(l, abs(m), theta, phi)
    if m == 0:
        return np.real(Y)
    if m > 0:
        return np.sqrt(2.0) * ((-1) ** m) * np.real(Y)
    return np.sqrt(2.0) * ((-1) ** m) * np.imag(Y)


def angular_wavefunction(l: int, m: int, theta, phi, real_form: bool = True):
    """
    Angular wavefunction Y_lm(theta, phi).
    """
    if real_form:
        return real_spherical_harmonic(l, m, theta, phi)
    return complex_spherical_harmonic(l, m, theta, phi)


def orbital_wavefunction(symbol: str, orbital: str, m: int, r, theta, phi, real_form: bool = True):
    """
    Full orbital psi(r, theta, phi) = R(r) Y(theta, phi).
    """
    data = ATOMIC_DB[symbol].orbitals[orbital]
    R = orbital_radial(symbol, orbital, r)
    Y = angular_wavefunction(data.l, m, theta, phi, real_form=real_form)
    return R * Y


def list_supported_elements():
    return list(ATOMIC_DB.keys())


def list_orbitals(symbol: str):
    return list(ATOMIC_DB[symbol].orbitals.keys())


def get_element(symbol: str) -> ElementData:
    return ATOMIC_DB[symbol]


def _orb(label, l, energy, rows):
    return OrbitalData(
        label=label,
        l=l,
        energy_hartree=energy,
        basis=tuple(BasisTerm(*row) for row in rows),
    )


